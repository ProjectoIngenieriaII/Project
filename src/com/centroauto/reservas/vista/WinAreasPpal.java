    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //Obtener la fila seleccionada en la tabla
        int row = this.tblDatos.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una sucurssal", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            //Obtener la informacion seleccionada
            AreasDTO a = (AreasDTO) this.tblDatos.getValueAt(row, AreasModel.OBJECT_COL);
            WinAreasForm winAreasForm = new WinAreasForm(false, a, this);
            winAreasForm.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

