    public void llenarDepartamentos() {
        try {
            List<Departamentos> lstDeptos = deptosDao.consultarDepartamentos();
            this.cmbDeptos.addItem("0 - Seleccione");
            for (Departamentos d : lstDeptos) {
                this.cmbDeptos.addItem(d.getIdDepto() + "   -   " + d.getNomDepto());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Consulta Departamentos: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void llenarCiudades() {
        ciudadDao = new CiudadesDao();
        Integer idDepto = Integer.parseInt(this.cmbDeptos.getSelectedItem().toString().split(" - ")[0].trim());
        try {

            if (idDepto > 0) {
                this.cmbCiudad.removeAllItems();
                this.cmbCiudad.addItem("0 - Seleccione");
                List<Ciudades> lstCiudades = ciudadDao.consultarCiudadesPorDepto(idDepto);
                for (Ciudades c : lstCiudades) {
                    this.cmbCiudad.addItem(c.getIdCiudad() + "   -   " + c.getNomCiudad());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Consulta Ciudades: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void llenarSucursales() {
        sucursalesDao = new SucursalesDao();
        Integer idCiudad = Integer.parseInt(this.cmbCiudad.getSelectedItem().toString().split(" - ")[0].trim());
        try {

            if (idCiudad > 0) {
                this.cmbSuc.removeAllItems();
                this.cmbSuc.addItem("0 - Seleccione");
                List<SucursalesDTO> lstSucursales = sucursalesDao.consultarSucursales(null, idCiudad);
                for (SucursalesDTO s : lstSucursales) {
                    this.cmbSuc.addItem(s.getIdCiudad() + " - " + s.getNomSucursal());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Consulta Sucursales: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void consultarDatos(String nomArea, Integer idCiudad) {
        try {
            List<AreasDTO> lstAreas;
            lstAreas = areasDao.consultarAreas(nomArea, idCiudad);
            AreasModel model = new AreasModel(lstAreas);
            this.tblDatos.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }
    }

